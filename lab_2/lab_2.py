# бұл condition-ға мысал, студенттің оқу үлгерімі туралы ақпарат шығарады
score = 48   # студенттің балл
if score >= 50:   # егер балл 50-ге тең немесе үлкен болса
    print("You passed! Your score is", score, "!")        # онда баллын және де тапсырғаны туралы ақпаратты шығарады
else:             # болмаса келесі мән орындалады
    print("You fail! Your score is", score, "!")          # мұнда баллын және де тапсырмағаны туралы ақпаратты шығарады
