# 1. Екі бүтін А және В саны берілген (А ≤ B бар). А-дан В-ға дейінгі барлық сандарды басып шығарыңыз.
print("A-ны еңгізіңіз: ")  # санды енгізіңіз деген жазуды шығарады
A = int(input())  # а-ны консольда еңгіземіз
print("B-ны еңгізіңіз: ")
B = int(input())  # b-ны консольда еңгіземіз
for ab in range(A, B + 1):
    print(ab, end=' ')


